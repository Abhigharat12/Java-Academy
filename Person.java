class Person {
    String Name;
    int Age;

    public Person(String Name,int Age){
        this.Name=Name;
        this.Age=Age;

    }

    public String getName(){
        return Name;
    }
    public void setName(String Name){
        this.Name=Name;
    }
    public int getAge(){
        return Age;
    }
    public void setAge(int Age){
        this.Age=Age;
    }
    

}